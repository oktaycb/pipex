#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	unsigned char	*p;
	unsigned char	ch;

	p = (unsigned char *)s;
	ch = c;
	while (len--)
	{
		*p = ch;
		p++;
	}
	return (s);
}
